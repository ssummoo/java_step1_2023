package chapter01;

public class CharEx3 {

	public static void main(String[] args) {

		//대문자 : 65 / 소문자 : 97
			int a=65;
			
			System.out.println(a);
			System.out.println((char)a); //명시적 형변환(ASCI 코드)
			System.out.println("============="); 
			
			int b=66;
			System.out.println(b);
			System.out.println((char)b); //명시적 형변환(ASCI 코드)
			System.out.println("============="); 
	
			int a2=97;
			System.out.println(a2);
			System.out.println((char)a2); //명시적 형변환(ASCI 코드)
			System.out.println("============="); 
	
	}

}
