package chapter01;

public class TypeInterface {

	public static void main(String[] args) {
		// var(참조형 변수선언) : 데이터의 형태를 보고 알아서 판단하여 타입을 지정함
		
		int i=10; 
		var j=10.0; //double
		var str="Test Good"; //String
				
				
		System.out.println(i);
		System.out.println(j);
		System.out.println(str);
	}

}
